package day9.operators;

public class RelationallOp {

	public static void main(String[] args) 
	{
		int num1=5,num2=55;
		System.out.println("Number 1 = "+num1);
		System.out.println("Number 2 = "+num2);
		System.out.println("(num1 == num2) ="+ (num1==num2));
		System.out.println("(num1 != num2) ="+ (num1!=num2));
		System.out.println("(num1 > num2) ="+ (num1>num2));
		System.out.println("(num1 < num2) ="+ (num1<num2));
		System.out.println("(num1 >= num2) ="+ (num1>=num2));
		System.out.println("(num1 <= num2) ="+ (num1<=num2));
	}

}
