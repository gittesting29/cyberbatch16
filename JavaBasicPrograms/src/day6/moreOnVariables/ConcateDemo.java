package day6.moreOnVariables;

public class ConcateDemo {
	public static void main(String[] args)
	{
		int num1=90, num2=90;
		System.out.println("Addition="+num1+num2);// concatenation. it will consider both num1 and num2 as string
		System.out.println("Addition="+(num1+num2));//addition according to BODMAS rule
	}

}
