package day33.exceptionhandling;
/**
* try block can have multiple catch block, but only one catch block will be executed 
* try can handle only one abnormal condition at a time
*/
public class ExceptionHandling4 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		try {
			int []a=new int[4];
			a[5]=4/0;	//as compiler first work on rhs it will chk for /by 0
		}
		catch(ArrayIndexOutOfBoundsException  e)
		{
			System.out.println("First catch block..");
			System.out.println(e);
		}
		catch(ArithmeticException  e)
		{
			System.out.println("Second catch block..");
			System.out.println(e);
		}
		catch(Exception  e)
		{
			System.out.println("Third catch block..");
			System.out.println(e);
		}
		catch(Throwable  e)
		{
			System.out.println("Fourth catch block..");
			System.out.println(e);
		}
		try {
			int []a=new int[4];
			a[5]=6;
		}
		/* if we write Throwable catch block here other catch statements
		 *  becomes unreachable code for compiler and error will generated by it
		 * catch(Throwable e) { System.out.println("Fourth catch block..");
		 * System.out.println(e); }
		 */
		catch(ArrayIndexOutOfBoundsException  e)
		{
			System.out.println("First catch block..");
			System.out.println(e);
		}
		catch(ArithmeticException  e)
		{
			System.out.println("Second catch block..");
			System.out.println(e);
		}
		catch(Exception  e)
		{
			System.out.println("Third catch block..");
			System.out.println(e);
		}
		
	}

}
