package day10.conditionalconstructs;

public class SimpleIELadder 
{

	public static void main(String[] args) 
	{
		int i=100;
		if(i==10)
			System.out.println("Value of i is 10");
		else if(i==20)
			System.out.println("Value of i is 20");
		else if(i==60)
			System.out.println("Value of i is 60");
		else if(i==50)
				System.out.println("Value of i is 20");
		else
			System.out.println("i is not present..");
	}

}
